#include "delay.h"
#include "include.h"

/*******************************************************************************
* @brief   delay num*1 ms.
* @param   num
* @retval  void
*******************************************************************************/
void Delay_ms(uint32_t num)
{
    uint16_t x,y;
    for(x = num;x > 0;x --)
        for(y = 3200;y > 0;y --);
}

/*******************************************************************************
* @brief   delay num*1 us.
* @param   num
* @retval  void
*******************************************************************************/
void Delay_us(uint32_t num)
{
    uint16_t x,y;
    for(x = num;x > 0;x --)
        for(y = 3;y > 0;y --);
}
